import { ImageBackground, StyleSheet, Text, TouchableOpacity, View } from "react-native";
import React from "react";
import { useRouter } from "expo-router";
import { StatusBar } from 'expo-status-bar';  // Correct import for Expo
import Animated, { FadeInDown, FadeInRight } from "react-native-reanimated";

const Page = () => {
  const router = useRouter();

  return (
    <View style={styles.container}>
      {/* StatusBar for Expo */}
      <StatusBar style="light" />
      
      {/* Background image */}
      <ImageBackground 
        source={require('../assets/images/getting-started.jpg')}  // Adjust path if necessary
        style={{ flex: 1 }} 
        resizeMode="cover"
      />
      
      {/* Content Wrapper */}
      <View style={styles.wrapper}>
        <Animated.Text style={styles.title} entering={FadeInRight.delay(300).duration(500)}>
          Stay Updated!
        </Animated.Text>
        <Animated.Text style={styles.description} entering={FadeInRight.delay(700).duration(500)}>
          Get breaking news and personalized updates directly to your feed.
        </Animated.Text>
        <Animated.View entering={FadeInDown.delay(1200).duration(500)}>
          <TouchableOpacity style={styles.btn} onPress={() => router.replace("/(tabs)")}>
            <Text style={styles.btnText}>Get Started</Text>
          </TouchableOpacity>
        </Animated.View>
      </View>
    </View>
  );
};

export default Page;

const styles = StyleSheet.create({
  container: {
    flex: 1,
  },
  wrapper: {
    flex: 1,
    justifyContent: 'flex-end',
    paddingBottom: 50,
    paddingHorizontal: 30,
    gap: 10,
    backgroundColor: 'rgba(0, 0, 0, 0.5)',
  },
  title: {
    color: '#fff',  // White color for the title
    fontSize: 24,
    fontWeight: '600',
    letterSpacing: 1.5,
    lineHeight: 36,
    textAlign: 'center',
  },
  description: {
    color: '#fff',  // White color for the description
    fontSize: 16,
    fontWeight: '500',
    letterSpacing: 1.2,
    lineHeight: 22,
    textAlign: 'center',
  },
  btn: {
    backgroundColor: '#2e78b7',  // Custom button color
    paddingVertical: 15,
    marginVertical: 20,
    alignItems: 'center',
    borderRadius: 10,
  },
  btnText: {
    color: '#fff',  // White text for button
    fontSize: 16,
    fontWeight: '700',
  }
});
